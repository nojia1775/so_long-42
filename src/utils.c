#include "../include/so_long.h"

size_t	modif_strlen(const char *str)
{
	size_t	n;
	int		i;

	i = 0;
	n = 0;
	while (str[i])
	{
		if (str[i] != '\n')
			n++;
		i++;
	}
	return (n);
}